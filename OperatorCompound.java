public class OperatorCompound {
    public static void main(String[] args) {
        int intValue = 10;
        intValue += 5;
        System.out.println(intValue);

        int res = intValue - 5;
        System.out.println(res);

        res = res * 5 ;
        System.out.println(res);

        res  = res / 5;
        System.out.println(res);

        res = res % 5;
        System.out.println(res);

    }


}
